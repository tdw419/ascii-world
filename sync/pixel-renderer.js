// ── Pixel Buffer Renderer ──────────────────────────────────────────────

/**
 * Render an ASCII string to an RGBA pixel buffer.
 * @param {string} asciiContent - The ASCII substrate content
 * @returns {{ width: number, height: number, data: Uint8Array }} RGBA pixel buffer
 */
export function renderToPixels(asciiContent) {
    if (FONT_DATA.size === 0) initFont();

    const data = new Uint8Array(PIXEL_W * PIXEL_H * 4);

    // Fill background
    for (let i = 0; i < PIXEL_W * PIXEL_H; i++) {
        data[i * 4 + 0] = COLORS.bg[0];
        data[i * 4 + 1] = COLORS.bg[1];
        data[i * 4 + 2] = COLORS.bg[2];
        data[i * 4 + 3] = COLORS.bg[3];
    }

    const lines = asciiContent.split('\n');

    for (let row = 0; row < Math.min(lines.length, GRID_H); row++) {
        const line = lines[row];
        const chars = [...line]; // Handle Unicode correctly
        for (let col = 0; col < Math.min(chars.length, GRID_W); col++) {
            const ch = chars[col];
            const codePoint = ch.codePointAt(0);
            const bitmap = FONT_DATA.get(codePoint) || FONT_DATA.get(0);
            const color = charColor(ch, chars[col - 1], chars[col + 1]);

            if (!bitmap) continue;

            // Render glyph pixels
            for (let gy = 0; gy < GLYPH_H; gy++) {
                const rowBits = bitmap[gy] || 0;
                for (let gx = 0; gx < GLYPH_W; gx++) {
                    if (rowBits & (1 << (GLYPH_W - 1 - gx))) {
                        const px = col * GLYPH_W + gx;
                        const py = row * GLYPH_H + gy;
                        const idx = (py * PIXEL_W + px) * 4;
                        data[idx + 0] = color[0];
                        data[idx + 1] = color[1];
                        data[idx + 2] = color[2];
                        data[idx + 3] = color[3];
                    }
                }
            }
        }
    }

    return { width: PIXEL_W, height: PIXEL_H, data };
}

/**
 * Render ASCII content to a PNG buffer (requires sharp or canvas).
 * Falls back to raw RGBA if neither is available.
 * @param {string} asciiContent
 * @returns {Promise<Buffer>} PNG buffer
 */
export async function renderToPNG(asciiContent) {
    const { width, height, data } = renderToPixels(asciiContent);

    try {
        // Try sharp (fast, no native GUI deps)
        const sharp = (await import('sharp')).default;
        return await sharp(Buffer.from(data), {
            raw: { width, height, channels: 4 }
        }).png().toBuffer();
    } catch {
        // Return raw RGBA buffer with dimensions header
        const header = Buffer.alloc(8);
        header.writeUInt32LE(width, 0);
        header.writeUInt32LE(height, 4);
        return Buffer.concat([header, Buffer.from(data)]);
    }
}

/**
 * Get pixel coordinate from character cell coordinate.
 */
export function cellToPixel(col, row) {
    return { x: col * GLYPH_W, y: row * GLYPH_H };
}

/**
 * Get character cell coordinate from pixel coordinate.
 */
export function pixelToCell(x, y) {
    return { col: Math.floor(x / GLYPH_W), row: Math.floor(y / GLYPH_H) };
}

