    getGlyph(ch) {
        const code = ch.codePointAt(0);
        const cached = this.cache.get(code);
        if (cached) return cached;
        // Unknown char → dotted box
        return [0x1F,0x11,0x11,0x11,0x11,0x11,0x1F,0,0,0];
    }

    /**
     * Draw a text string to a PixelBuffer at pixel coordinates.
     * @param {PixelBuffer} buf
     * @param {number} px - pixel x
     * @param {number} py - pixel y
     * @param {string} text
     * @param {number[]} color - [r, g, b] or [r, g, b, a]
     */
    drawText(buf, px, py, text, color) {
        const [r, g, b, a = 255] = color;
        let cx = px;
        for (const ch of text) {
            const bitmap = this.getGlyph(ch);
            for (let gy = 0; gy < this.glyphH; gy++) {
                const row = bitmap[gy] || 0;
                for (let gx = 0; gx < this.glyphW; gx++) {
                    if (row & (1 << (this.glyphW - 1 - gx))) {
                        buf.setPixel(cx + gx, py + gy, r, g, b, a);
                    }
                }
            }
            cx += this.glyphW;
        }
    }

    /**
     * Draw text at cell coordinates (col, row) instead of pixel coordinates.
     */
    drawTextCell(buf, col, row, text, color) {
        this.drawText(buf, col * this.glyphW, row * this.glyphH, text, color);
    }
