// pixel-renderer.js - Renders ASCII substrates to pixel buffers
// The bridge between Stratum 1 (text cells) and Stratum 2 (pixel grid)
//
// ASCII characters → pixel glyphs in an 80x24 RGBA buffer
// Same data, different medium. AI writes text, humans see pixels.
//
// Glyph rendering uses a built-in 6x10 bitmap font embedded as hex.
// Each character maps to a 6x10 pixel region in the output buffer.

const GRID_W = 80;
const GRID_H = 24;
const GLYPH_W = 6;
const GLYPH_H = 10;
const PIXEL_W = GRID_W * GLYPH_W;  // 480
const PIXEL_H = GRID_H * GLYPH_H;  // 240

// ── Color Palette ──────────────────────────────────────────────────────

const COLORS = {
    bg:       [0x0a, 0x0a, 0x0f, 0xff],
    fg:       [0xc9, 0xd1, 0xd9, 0xff],
    green:    [0x3f, 0xb9, 0x50, 0xff],
    red:      [0xf8, 0x51, 0x49, 0xff],
    yellow:   [0xd2, 0x99, 0x22, 0xff],
    cyan:     [0x00, 0xd4, 0xff, 0xff],
    blue:     [0x58, 0xa6, 0xff, 0xff],
    dim:      [0x48, 0x4f, 0x58, 0xff],
    border:   [0x30, 0x36, 0x3d, 0xff],
    bar_fill: [0x23, 0x86, 0x36, 0xff],
    bar_empty:[0x16, 0x1b, 0x22, 0xff],
};

// Character → color mapping based on semantic meaning
function charColor(ch, prevCh, nextCh) {
    // Status indicators
    if (ch === '●') return COLORS.green;
    if (ch === '○') return COLORS.dim;
    if (ch === '◉') return COLORS.red;
    if (ch === '◐' || ch === '◑') return COLORS.yellow;
    if (ch === '✗') return COLORS.red;

    // Box drawing characters
    const code = ch.charCodeAt(0);
    if (code >= 0x2500 && code <= 0x257F) return COLORS.border;  // Box Drawing
    if (code >= 0x2550 && code <= 0x256C) return COLORS.cyan;    // Double borders
    if (code >= 0x2580 && code <= 0x259F) {                      // Block elements
        if (ch === '█' || ch === '▓') return COLORS.bar_fill;
        if (ch === '░') return COLORS.bar_empty;
        // Sparkline blocks
        return COLORS.cyan;
    }

    // Bracket keys [A]
    if (ch === '[' || ch === ']') return COLORS.cyan;

    return COLORS.fg;
}

